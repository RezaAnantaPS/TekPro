package aggregation;

public class Village {
	private String name;
	private String gokage;
	
	public Village(String name, String gokage) {
		this.name = name;
		this.gokage = gokage;
	}
	
	public String getname() {
		return name;
	}
	public String getgokage() {
		return gokage;
	}
}
